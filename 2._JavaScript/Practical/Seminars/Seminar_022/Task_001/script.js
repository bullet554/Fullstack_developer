const mockDataBase = [
    {
        title: "Новость 1",
        content: "Lorem ipsum dolor sit amet consectetur, adipisicing elit. Nisi, eos? Vel, vero!"
    },
    {
        title: "Новость 2",
        content: "Lorem ipsum dolor, sit amet consectetur adipisicing elit. Enim, eos."
    }
];


function fetchNews() {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            if (Math.random() < 0.1) {
                reject('Ошибка загрузки данных');
            } else {
                resolve(mockDataBase);
            }
        }, 2000);
    })
}

loadButton.addEventListener('click', () => {
    fetchNews()
        .then(data => {
            data.forEach(function (item) {
                const newsBlock = document.createElement('p');
                newsBlock.innerHTML = `
                    <h2>${item.title}</h2>
                    <p>${item.content}</p>
                `;
                newsContainer.appendChild(newsBlock);
            });
        }).catch((err) => {
            newsContainer.textContent = `Ошибка: ${err}`;
        });
});